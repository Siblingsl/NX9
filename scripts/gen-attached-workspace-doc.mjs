/**
 * gen-attached-workspace-doc.mjs
 *
 * 从 ATTACHED_WORKSPACE_REGISTRY 生成 docs/NX9-ATTACHED-WORKSPACE-REGISTRY.md。
 * MUST: 每次修改 ATTACHED_WORKSPACE_REGISTRY 后运行。
 *
 * Usage: node scripts/gen-attached-workspace-doc.mjs
 */

import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

// Read the compiled registry from @nx9/shared (built first)
const registryPath = join(root, 'packages', 'shared', 'src', 'catalog', 'attached-workspace.ts');
const src = readFileSync(registryPath, 'utf-8');

// Parse the registry from the TS source (simple extraction)
const kindPattern = /'([\w-]+)':\s*\{/g;
const wsPattern = /workspaceType:\s*'([\w-]+)'/;
const fcPattern = /functionalClass:\s*'([\w-]+)'/;
const phasePattern = /phase:\s*'(\w+)'/;
const attachPattern = /attachToNode:\s*(true|false)/;

const kinds = [];
let match;
while ((match = kindPattern.exec(src)) !== null) {
  const kind = match[1];
  const start = match.index;
  const block = src.slice(start, src.indexOf('}', start) + 1);
  const ws = wsPattern.exec(block)?.[1] ?? '?';
  const fc = fcPattern.exec(block)?.[1] ?? '?';
  const phase = phasePattern.exec(block)?.[1] ?? '?';
  const attach = attachPattern.exec(block)?.[1] === 'true';
  kinds.push({ kind, ws, fc, phase, attach });
}

kinds.sort((a, b) => a.phase.localeCompare(b.phase) || a.kind.localeCompare(b.kind));

const lines = [
  '# NX9 Attached Workspace Registry',
  '',
  '> Auto-generated by `scripts/gen-attached-workspace-doc.mjs` from `ATTACHED_WORKSPACE_REGISTRY`.',
  `> Generated: ${new Date().toISOString().slice(0, 10)}`,
  '',
  '| kind | workspaceType | functionalClass | phase | attachToNode |',
  '|------|--------------|-----------------|-------|-------------|',
  ...kinds.map(
    (k) => `| \`${k.kind}\` | ${k.ws} | ${k.fc} | ${k.phase} | ${k.attach ? '✅' : '❌'} |`,
  ),
  '',
  '## Legend',
  '',
  '- **workspaceType**: The type of workspace content rendered in the node-attached prompt bar.',
  '- **functionalClass**: The functional class determining canvas summary style.',
  '- **phase**: Implementation phase (P1-P4). P1 items are fully functional.',
  '- **attachToNode**: ✅ = node-attached workspace bar is shown on selection.',
  '',
  '---',
  '',
  '*Document auto-generated. Do not edit manually.*',
];

writeFileSync(join(root, 'docs', 'NX9-ATTACHED-WORKSPACE-REGISTRY.md'), lines.join('\n') + '\n');
console.log('✅ Generated docs/NX9-ATTACHED-WORKSPACE-REGISTRY.md');
